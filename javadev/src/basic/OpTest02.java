package basic;

//관계연산자 
public class OpTest02 {

	public static void main(String[] args) {
		int a = 7;
		int b = 4;
		
		System.out.println(a > b); //관계 연산자의 결과 타입 boolean
		System.out.println(a < b); 
		System.out.println(a >= b); 
		System.out.println(a <= b); 
		System.out.println(a == b); 
		System.out.println(a != b); 
		 

	}

}
